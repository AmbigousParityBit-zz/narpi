func _TestRunesArrayFromTestImages(t *testing.T) {
	pstfix := []string{
		"1.png", // 1 - "E"; 2 - "|" on black background
		"2.png",
		"1.jpg",
		"2.jpg",
	}

	prf := "./testimages/testrunes/testrunes"
	narpi := NARPImage{}

	for _, v := range pstfix {
		fnm := prf + v
		err := narpi.Load(fnm, false)
		if err != nil {
			t.Fatalf("problem with constructing NARPImage from file <%s>", fnm)
		}
		log.Printf("constructed NARPImage from file <%v>", fnm)
		narpi.Print()
	}
}

